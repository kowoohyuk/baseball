package baseball.dto;

public class PlayerDto {

    private Long id;
    private String name;
    private String role;
    private boolean status;

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getRole() {
        return role;
    }

    public boolean getStatus() {
        return status;
    }
}
